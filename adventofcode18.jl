function initialize()
	for (jdx, line) in enumerate(split(inpt))
		for (idx, c) in enumerate(line)
			grid[idx+1, jdx+1] = c == '#'
			println(idx+1, " ", jdx+1, " ", grid[idx+1, jdx+1])
		end
	end
	# part two: four lights in the corners are stuck to "on"
	grid[2,2] = true
	grid[2,101] = true
	grid[101,2] = true
	grid[101,101] = true
end

function update()
	neighbors = Array{Int64}(size(grid))
	for j = 2:(size(grid)[2]-1), i = 2:(size(grid)[1]-1)
		neighbors[i,j] = sum([grid[i+y, j+x]  for x in -1:1, y in -1:1])-grid[i,j]
	end
	for j = 2:(size(grid)[2]-1), i = 2:(size(grid)[1]-1)
		if grid[i,j]
			if !(neighbors[i,j] == 2 || neighbors[i,j] == 3)
				grid[i,j] = false
			end
		elseif !grid[i,j]
			if neighbors[i,j] == 3
				grid[i,j] = true
			end
		end
	end
	# part two: four lights in the corners are stuck to "on"
	grid[2,2] = true
	grid[2,101] = true
	grid[101,2] = true
	grid[101,101] = true
end

